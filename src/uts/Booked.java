public class Booked {
    public String id;
    public String nama;
    public String jadwal;
    public String kursi;
    public String kelas;
    Booked(String id, String nama, String jadwal, String kursi, String kelas) {
        this.id = id;
        this.nama = nama;
        this.jadwal = jadwal;
        this.kursi = kursi;
        this.kelas = kelas;
    }
}