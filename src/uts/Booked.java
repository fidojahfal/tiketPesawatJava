public class Booked {
    String id;
    String nama;
    String jadwal;
    String kursi;
    String kelas;
    Booked(String id, String nama, String jadwal, String kursi, String kelas) {
        this.id = id;
        this.nama = nama;
        this.jadwal = jadwal;
        this.kursi = kursi;
        this.kelas = kelas;
    }
}